package com.talenteck.ttanalytics;

public class LiveReportTableRow {
	String quantileNumber;
	Double applied;
	Double interviewed;
	Double offered;
	Double accepted;
	Double hired;
	Double terminated;
	Double eligibility;
	Double turnover;

	public String getQuantileNumber() {
		return quantileNumber;
	}
	public void setQuantileNumber(String quantileNumber) {
		this.quantileNumber = quantileNumber;
	}
	public Double getApplied() {
		return applied;
	}
	public void setApplied(Double applied) {
		this.applied = applied;
	}
	public Double getinterviewed() {
		return interviewed;
	}
	public void setinterviewed(Double interviewed) {
		this.interviewed = interviewed;
	}
	public Double getOffered() {
		return offered;
	}
	public void setOffered(Double offered) {
		this.offered = offered;
	}
	public Double getAccepted() {
		return accepted;
	}
	public void setAccepted(Double accepted) {
		this.accepted = accepted;
	}
	public Double getHired() {
		return hired;
	}
	public void setHired(Double hired) {
		this.hired = hired;
	}
	public Double getTerminated() {
		return terminated;
	}
	public void setTerminated(Double terminated) {
		this.terminated = terminated;
	}
	public Double getEligibility() {
		return eligibility;
	}
	public void setEligibility(Double eligibility) {
		this.eligibility = eligibility;
	}
	public Double getTurnover() {
		return turnover;
	}
	public void setTurnover(Double turnover) {
		this.turnover = turnover;
	}
}
